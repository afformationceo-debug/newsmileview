def patch_file(path, patch_dict):
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    changed = 0
    for item in data:
        text = item.get("text", "")
        if KO_RE.search(str(item.get("translated", ""))):
            if text in patch_dict:
                item["translated"] = patch_dict[text]
                changed += 1
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    remaining = sum(1 for x in data if KO_RE.search(str(x.get("translated", ""))))
    print(f"  {os.path.basename(path)}: +{changed} 패치, 잔존={remaining}")
    return remaining


