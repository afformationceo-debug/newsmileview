def patch_by_key(page, key_map):
    fp = os.path.join(BASE, f"{page}.json")
    with open(fp, 'r', encoding='utf-8') as f:
        data = json.load(f)

    patched = 0
    for item in data:
        k = item.get('key', '')
        if k in key_map:
            item['translated'] = key_map[k]
            patched += 1

    with open(fp, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # Count remaining Korean
    remaining = sum(1 for item in data if ko.search(item.get('translated', '')))
    return patched, remaining


for page, key_map in BYKEY.items():
    n, rem = patch_by_key(page, key_map)
    print(f"{page}: patched={n}, remaining_korean={rem}")
