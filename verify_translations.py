def count_remaining_korean(filepath):
    """Count Korean-containing lines in HTML, excluding script/style/comment blocks."""
    ko = re.compile(r'[\uAC00-\uD7A3\u3131-\u314E\u314F-\u3163]')
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    in_script = in_style = False
    ko_lines = []
    for i, line in enumerate(lines, 1):
        sl = line.strip().lower()
        if '<script' in sl:
            in_script = True
        if '</script>' in sl:
            in_script = False
            continue
        if '<style' in sl:
            in_style = True
        if '</style>' in sl:
            in_style = False
            continue
        # skip HTML comments
        if sl.startswith('<!--') and '-->' in sl:
            continue
        if not in_script and not in_style and ko.search(line):
            # skip lines that only have Korean in image src filenames
            stripped = re.sub(r'src="[^"]*"', '', line)
            if ko.search(stripped):
                ko_lines.append((i, line.rstrip()))
    return ko_lines


def apply_fixes(html, fixes):
    for old, new in fixes:
        html = html.replace(old, new)
    return html


def verify_page(page, suffix='_tw'):
    path = os.path.join(BASE, f'{page}{suffix}.html')
    if not os.path.exists(path):
        print(f'  SKIP {page}{suffix}.html (not found)')
        return None

    with open(path, 'r', encoding='utf-8') as f:
        html = f.read()

    before = len(count_remaining_korean(path))

    if suffix == '_tw':
        page_fixes = PAGE_FIXES_TW.get(page, [])
        universal = UNIVERSAL_TW
    else:
        page_fixes = PAGE_FIXES_JP.get(page, [])
        universal = UNIVERSAL_JP

    # Apply page-specific fixes FIRST (before universal changes micro-terms)
    html = apply_fixes(html, page_fixes)
    # Then apply universal fixes for remaining micro-terms
    html = apply_fixes(html, universal)

    with open(path, 'w', encoding='utf-8') as f:
        f.write(html)

    after_lines = count_remaining_korean(path)
    return before, len(after_lines), after_lines


