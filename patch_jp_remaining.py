def patch_file(filepath, patch_dict):
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)

    patched = 0
    # Build reverse: key -> translated value
    for item in data:
        key = item.get('key', '')
        # Find by key match in patch_dict values
        for trans, k in patch_dict.items():
            if k == key and item.get('translated') == item.get('text'):
                item['translated'] = trans
                patched += 1
                break

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    return patched


def count_remaining(filepath):
    import re
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    ko_pattern = re.compile(r'[가-힣ㄱ-ㅎㅏ-ㅣ]')
    count = 0
    for item in data:
        translated = item.get('translated', '')
        if ko_pattern.search(translated):
            count += 1
    return count


