KO_RE = re.compile(r"[가-힣ㄱ-ㅎㅏ-ㅣ]")


def translate_text(text, dic):
    """사전 기반 번역 - 완전 일치 우선, 없으면 부분 치환"""
    if not text or not KO_RE.search(text):
        return text
    # 완전 일치
    if text in dic:
        return dic[text]
    # 부분 치환 (긴 키 먼저)
    result = text
    for k in sorted(dic.keys(), key=len, reverse=True):
        result = result.replace(k, dic[k])
    return result


def process_file(ko_path, out_path, dic):
    with open(ko_path, encoding="utf-8") as f:
        data = json.load(f)
    for item in data:
        item["translated"] = translate_text(item.get("text", ""), dic)
    os.makedirs(os.path.dirname(out_path), exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    ko_left = sum(1 for x in data if KO_RE.search(str(x.get("translated", ""))))
    print(f"  -> {os.path.basename(out_path)}: {len(data)}개, 한국어잔존={ko_left}")


